##Loops and Iteration##
##Definite Loops
# >because they execut and exact number of times
# >definite loops iterate through the members of a set

##A Simple Definite Loop
for i in [5, 4, 3, 2, 1] :
    print(i) # i is iterate variable
print('Blastoff!')

##A Definite Loop with Strings
friends = ['Joseph', 'Glenn', 'Sally']
for friend in friends :
    print('Hally New Year:', friend)
print('Done!')
